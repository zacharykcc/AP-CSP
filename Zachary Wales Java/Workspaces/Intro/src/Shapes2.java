
public class Shapes2 {
	public static void main(String[] args) {
		
		//Square
		System.out.println("*****");
		System.out.println("*   *");
		System.out.println("*   *");
		System.out.println("*   *");
		System.out.println("*****");
		System.out.print("\n");

		//Ellipse
		System.out.println("  ***  ");
		System.out.println(" *   *");
		System.out.println("*     *");
		System.out.println(" *   *");
		System.out.println("  ***  ");
		System.out.print("\n");

		//Arrow
		System.out.println("  *  ");
		System.out.println(" *** ");
		System.out.println("*****");
		System.out.println("  *  ");
		System.out.println("  *  ");
		System.out.print("\n");

		//Rhombus
		System.out.println("  *  ");
		System.out.println(" * * ");
		System.out.println("*   *");
		System.out.println(" * * ");
		System.out.print("  *  ");
		
		
		
	}
}
